import Foundation
import UIKit

struct SharedViewValues {
    static func getLightOrangeColor () -> UIColor {
        return UIColor(red: 0.985, green: 0.719, blue: 0.328, alpha: 1.000)
    }
    static func getDarkOrangeColor() -> UIColor {
        return UIColor(red: 0.976, green: 0.732, blue: 0.020, alpha: 1.000)
    }
    static func getMaterialGreenColor() -> UIColor {
        return UIColor(red: 0.0, green: 150/255, blue: 136/255, alpha: 1.000)
    }
}
